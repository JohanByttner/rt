#include "rt.h"
#include "cpugpu"

void		t_float_cross(t_float3 res, const t_float3 a, const t_float3 b)
{
	res[0] = a[1] * b[2] - a[2] * b[1];
	res[1] = a[2] * b[0] - a[0] * b[2];
	res[2] = a[0] * b[1] - a[1] * b[0];
}
